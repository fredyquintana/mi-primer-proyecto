public class Calculadora {
    
    // Method to add two integers
    int sumar(int a, int b) {
        return a + b;
    }
    // Method to add three integers
    int sumar(int a, int b, int c) {
        return a + b + c;
    }
    // Method to add two floating point numbers
    double sumar(double a, double b) {
        return a + b;
    }
}


